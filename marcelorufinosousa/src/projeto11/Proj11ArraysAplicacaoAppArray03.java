package projeto11;

public class Proj11ArraysAplicacaoAppArray03 {
public static void main(String[] args) {
		
	
		int[] numeros = new int[]  {17,13,18,25,39,17};
		
		for( int elemento : numeros) {			
			System.out.println(elemento);			
		}	
		

		
		numeros = new int[]{2,5,7,1,0};
		
	
		System.out.println();
		
		for( int elemento : numeros) {			
			System.out.println(elemento);			
		}

    }

}
